package org.courses.tour.transport;

public abstract class Transport {
    protected double cost;
    private String name;


    public Transport(double cost, String name) {
        this.cost = cost;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getCost() {
        return cost;
    }

    public void setCost(double cost) {
        this.cost = cost;
    }

    @Override
    public String toString() {
        return name;
    }
}
